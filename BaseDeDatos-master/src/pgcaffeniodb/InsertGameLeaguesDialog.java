    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        String league = "";
        String game = "";
        
        
        String game_code = iGame_Code.getSelectedItem().toString();
        String league_name = iLeague_Name.getSelectedItem().toString();
        String sql1 = "select league_id from leagues where league_name = \'" + league_name + "\'";
        String sql2 = "select game_code from game where game_name = \'" + game_code + "\'";
        try {
            ResultSet s = db.query(sql1);
            s.next();
            ResultSet w = db.query(sql2);
            w.next();
            league += s.getString("league_id");
            game += w.getString("game_code");
        } catch (SQLException ex) {
            Logger.getLogger(InsertGameLeaguesDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        

        StringBuilder sql
                = new StringBuilder("INSERT INTO games_have_leagues (league_id, "
                        + "game_code) VALUES (\'");
        sql.append(league);
        sql.append("\',\'");
        sql.append(game);
        sql.append("\')");
        System.out.println( sql.toString() );

        try {
            db.update( sql.toString() );
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error inserting relation.");
            System.out.println( ex.getMessage() );
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

