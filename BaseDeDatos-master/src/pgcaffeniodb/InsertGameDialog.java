    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        String oDetails = " ";
        String g_name = ig_name.getText();
        String g_desc = ig_desc.getText();
        oDetails = iOther_Details.getText();

        StringBuilder sql
                = new StringBuilder("INSERT INTO game(game_code, game_name, "
                        + "game_desc, other_details) VALUES (\'");
        sql.append(g_name);
        sql.append("\',\'");
        sql.append(g_desc);
        sql.append("\',\'");
        sql.append(oDetails);
        sql.append("\')");
        System.out.println(sql.toString());

        try {
            db.update(sql.toString());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error inserting game.");
            System.out.println(ex.getMessage());
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

